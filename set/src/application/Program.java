/*Um site de internet registra um log de acessos dos usuários. Um
registro de log consiste no nome de usuário (apenas uma palavra) e o
instante em que o usuário acessou o site no padrão ISO 8601,
separados por espaço, conforme exemplo. Fazer um programa que leia
o log de acessos a partir de um arquivo, e daí informe quantos usuários
distintos acessaram o site.
 * 
 * */

package application;

import java.io.BufferedReader;
import java.io.FileReader;
import java.io.IOException;
import java.time.Instant;
import java.util.Date;
import java.util.HashSet;
import java.util.Scanner;
import java.util.Set;

import entities.LogEntry;

public class Program {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		
		System.out.print("Enter file full path: ");
		String path = sc.nextLine();  //irá ler o caminho completo do arquivo
		
		try (BufferedReader br = new BufferedReader(new FileReader(path))) { //irá abrir e ler o arquivo

			Set<LogEntry> set = new HashSet<>();
			
			String line = br.readLine(); //ler a primeira linha
			while (line != null) { //enquanto a linha for diferente de nulo...
			
				String[] fields = line.split(" "); //recortar a string baseado em um espaço em branco
				String username = fields[0]; //receberá fields na posição 0
				Date moment = Date.from(Instant.parse(fields[1]));
				
				set.add(new LogEntry(username, moment));
				//inserindo os dados no set. Os nomes repetidos serão barrados pelo próprio set
				
				line = br.readLine(); //ler a próxima linha 
			}	
			System.out.println("Total users: " + set.size()); // informa o tamanho do conjunto

		} catch (IOException e) {
			System.out.println("Error: " + e.getMessage());
		}
		sc.close();
	}
}